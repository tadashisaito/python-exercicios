"""
Exercício 07 – Média do aluno

Programa que lê duas notas de um aluno
e calcula a sua média.
"""

n1 = float (input ('Digite a sua primeira nota: '))
n2 = float (input ('Digite a sua segunda nota: '))
media = (n1 + n2) / 2

print (f'Com base na sua primeira nota {n1:.1f}, e a segunda nota {n2:.1f} \nA sua média é igual a {media:.1f}')
